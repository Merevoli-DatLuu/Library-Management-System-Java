    private ArrayList<String> getMaSach(){
        ArrayList<String> arrMS =new ArrayList<>();
        for(int i=0;i<tblCTPN.getRowCount();i++){
            String maSach = (String) tblCTPN.getModel().getValueAt(i, 1);
            arrMS.add(maSach);
        }
        return arrMS;
    }
    private ArrayList<Integer> getSoLuong(){
        ArrayList<Integer> arrSL =new ArrayList<>();
        for(int i=0;i<tblCTPN.getRowCount();i++){
            int soLuong = Integer.parseInt((String) tblCTPN.getModel().getValueAt(i, 2));
            arrSL.add(soLuong);
        }
        return arrSL;
    }
        
    private void addButtonMouseClicked(java.awt.event.MouseEvent evt) {
        String maNV = maNV_Label.getText();
        String maNCC = maNCC_Label.getText();
        String ngayNhap = ngayNhap_Label.getText();
        String maPhieuNhap = dp.next_maPhieuNhap(pnBUS.getPKey());
        ArrayList<String> maSach = getMaSach();
        ArrayList<Integer> soLuong =getSoLuong();
        System.out.println(maSach);
        System.out.println(soLuong);
        if (check_input(maNV, maNCC, ngayNhap)){
            System.out.println("Nhập Thành Công");
            if (pnBUS.add(maPhieuNhap, ngayNhap, maSach, soLuong, maNV, maNCC, 0, 0)){
                new AlertGUI(3, "Success", "Nhập Phiếu Nhập Thành Công!!!", "Quay Lại").setVisible(true);
                this.dispose();
            }
        }
        else{
            System.err.println(error_mess);
            System.out.println("Nhập Thất Bại");
            new AlertGUI(2, "Error", error_mess, "Quay Lại").setVisible(true);
            // Alert Form
        }
        
        System.out.println(maNV);
        System.out.println(maNCC);
        System.out.println(ngayNhap);
        
    }

    
