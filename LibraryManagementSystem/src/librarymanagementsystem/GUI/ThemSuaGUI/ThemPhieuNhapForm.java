    private boolean check_inputTable(String maPN, String maSach){
        if (maPN.equals("")){
            error_mess = "Mã Phiếu Nhập trống!!!";
            return false;
        }
        if (maSach.equals("")){
            error_mess = "Mã Sách trống!!!";
            return false;
        }
        if (dp.check_maPhieuNhap(maPN)!=true){
            error_mess = "Mã Phiếu Nhập nhập sai!!!";
            return false;
        }
        if (dp.check_maSach(maSach)!=true){
            error_mess = "Mã Sách nhập sai!!!";
            return false;
        }
        return true;
    }
    
    private boolean check_input(String maNV,String maNCC, String ngayNhap){
        if (maNV.equals("")){
            error_mess = "Mã Nhân Viên trống!!!";
            return false;
        }
        if (maNCC.equals("")){
            error_mess = "Mã Nhà Cung Cấp trống!!!";
            return false;
        }
        if (ngayNhap.equals("")){
            error_mess = "Ngày Nhập trống!!!";
            return false;
        }
        if (dp.check_maQuanLy(maNV)){
            error_mess = "Mã Nhân Viên nhập sai!!!";
            return false;
        }
        if (dp.check_maNCC(maNCC)){
            error_mess = "Mã Nhà Cung Cấp nhập sai!!!";
            return false;
        }
        if (dp.check_ngaythangnam(ngayNhap)){
            error_mess = "Ngày Nhập nhập sai!!!";
            return false;
        }
        
        return true;
    }
    
